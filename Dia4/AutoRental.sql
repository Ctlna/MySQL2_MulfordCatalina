create database AutoRental;
use AutoRental;

drop table sucursal;

create table sucursal(
	id int primary key auto_increment not null,
    direccion varchar(20) not null,
    celular int(10) not null,
    correo varchar(100) not null,
    telefono int(10),
    ciudad varchar(30) not null
);
create table empleado(
	id int primary key auto_increment not null,
    cedula int(10) not null,
    nombre varchar(35) not null,
    apellido varchar(35) not null,
    celular int(10) not null,
    ciudad_residencia varchar(30) not null,
    correo varchar(30) not null,
    direccion varchar(20) not null,
    id_sucursal int not null,
    foreign key(id_sucursal) references sucursal(id)
);
create table cliente(
	id int primary key auto_increment not null,
    cedula int(10) not null,
    nombre varchar(35) not null,
    apellido varchar(35) not null,
    celular int(10) not null,
    ciudad_residencia varchar(30) not null,
    correo varchar(30) not null,
    direccion varchar(20) not null
);
create table vehiculo(
	id int primary key auto_increment not null,
    tipo varchar(30) not null,
    referencia int(30) not null,
    modelo year not null,
    placa int(6) not null,
    capacidad int(2) not null,
    sunroof enum('Si','No') not null,
    puertas int(2) not null,
    color varchar(10) not null,
    motor varchar(10) not null
);
create table alquiler(
	id int primary key auto_increment not null,
    idSucursal_salida int not null,
    idSucursal_llegada int not null,
    idEmpleado int not null,
    idVehiculo int not null,
    idCliente int not null,
    valor_semana int(7) not null,
    valor_dia int(7) not null,
    fecha_salida date not null,
    esperada_llegada date not null,
    fecha_llegada date not null,
    valor_cotizado int not null,
    descuento int not null,
    valor_pagado int not null,
    foreign key(idSucursal_salida) references sucursal (id),
    foreign key(idSucursal_llegada) references sucursal (id),
    foreign key(idEmpleado) references empleado (id),
    foreign key(idVehiculo) references vehiculo (id),
    foreign key(idCliente) references cliente (id)
);

